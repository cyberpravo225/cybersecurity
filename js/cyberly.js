// 🎲 Утилита: получить случайное слово по сложности
export function getRandomWord(difficulty) {
  let pool;

  switch (difficulty) {
    case "easy":
      pool = easyWords;
      break;
    case "medium":
      pool = mediumWords;
      break;
    case "hard":
      pool = hardWords;
      break;
    default:
      pool = [...easyWords, ...mediumWords, ...hardWords];
  }

  return pool[Math.floor(Math.random() * pool.length)];
}
